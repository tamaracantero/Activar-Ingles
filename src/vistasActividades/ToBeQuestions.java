    private void botonCorregirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCorregirActionPerformed
        nota = 0.0;
        correctas = 0;
        incorrectas = 0;
        for (int j = 0; j < es.size(); j++) {
            JTextField e = es.get(j);
            if (j == 1 - 1 || j == 2 - 1 || j==11-1) {
                comprobar(e, j, "is");
            } else if (j == 6 - 1 || j == 7 - 1 || j == 8 - 1 || j == 9 - 1 || j == 10 - 1 || j == 4 - 1) {
                comprobar(e, j, "are");
            } else {
                comprobar(e, j, "am");
            }
        }
        
        for (int i = 0; i < rs.size(); i++) {
            JTextField r = rs.get(i);
            if (i == 1 - 1 || i == 2 - 1 || i==11-1) {
               
                comprobarRespuestas(r, i, "is",ps.get(i).getText());
            } else if (i == 6 - 1 || i == 7 - 1 || i == 8 - 1 || i == 9 - 1 || i == 10 - 1 || i == 4 - 1) {
                comprobarRespuestas(r, i, "are",ps.get(i).getText());
            } else {
                if(i==5){
                    comprobarRespuestasPositivo(r, i, "am",ps.get(i).getText());
                    
                    comprobarRespuestasNegativo(r, i, "am",ps.get(i).getText());
                    
                }
            }
        }
        textoCorrectas.setText("correctas: " + correctas + "/16");
        textoIncorrectas.setText("incorrectas: " + incorrectas + "/16");
        textoNota.setText("NOTA: " + nota);

    }//GEN-LAST:event_botonCorregirActionPerformed

