    private void botonCorregirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCorregirActionPerformed
        nota = 0.0;
        correctas = 0;
        incorrectas = 0;
        for (int j = 0; j < es.size(); j++) {
            JTextField e = es.get(j);
            if (j == 2 - 1 || j == 3 - 1 || j == 4 - 1 || j == 9 - 1 || j == 15 - 1 || j == 16 - 1 || j==11-1) {
                comprobar(e, j, "have");
            } else {
                comprobar(e, j, "has");
            }
        }
        textoCorrectas.setText("correctas: " + correctas + "/16");
        textoIncorrectas.setText("incorrectas: " + incorrectas + "/16");
        textoNota.setText("NOTA: " + nota);
    }//GEN-LAST:event_botonCorregirActionPerformed

