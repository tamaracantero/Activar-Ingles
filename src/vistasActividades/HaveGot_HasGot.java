    public void comprobar(JTextField e, int j, String verbo) {
        cs.get(j).setVisible(true);
            
        if (e.getText().contains(verbo.toLowerCase())) {
            
            cs.get(j).setIcon(new ImageIcon(getClass().getResource("/imagenes/tildeVerde2.png")));
            correctas++;
            nota+=puntajeXEjer; 
        } else {
            cs.get(j).setIcon(iconNegativo);
            incorrectas++;
        }
    }
